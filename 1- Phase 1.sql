create or replace TABLE ONLINE_STORE_RAW.ONLINE_STORE_RAW.CUSTOMERS (
	CUSTOMER_ID NUMBER(38,0) NOT NULL,
	CUSTOMER_NAME VARCHAR(100),
	ZIP_CODE VARCHAR(100),
	primary key (CUSTOMER_ID)
);

create or replace TABLE ONLINE_STORE_RAW.ONLINE_STORE_RAW.CUSTOMER_ORDER (
	ORDER_ID NUMBER(38,0) NOT NULL,
	CUSTOMER_ID NUMBER(38,0),
	ORDER_DATE VARCHAR(16777216),
	primary key (ORDER_ID)
);

create or replace TABLE ONLINE_STORE_RAW.ONLINE_STORE_RAW.ORDER_DETAILS (
	ORDER_ID NUMBER(38,0) NOT NULL,
	PRODUCT_ID NUMBER(38,0),
	UNIT_PRICE NUMBER(38,0),
