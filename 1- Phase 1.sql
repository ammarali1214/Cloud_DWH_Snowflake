	DISCOUNT NUMBER(38,0),
	TOTAL_PRICE NUMBER(38,0),
	primary key (ORDER_ID),
	foreign key (PRODUCT_ID) references ONLINE_STORE_RAW.ONLINE_STORE_RAW.PRODUCTS(PRODUCT_ID)
);

create or replace TABLE ONLINE_STORE_RAW.ONLINE_STORE_RAW.PRODUCTS (
